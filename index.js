function add(x,y) {
  return x+y;
}
function subtract(x,y) {
  return x-y;
}
function multiply(x,y) {
  return x*y;
}
function divide(x,y) {
  return x / y;
}
function parseToInt(x)
{
  return parseInt(x,10);
}
console.log(add(1,2));
console.log(subtract(3,1));
console.log(multiply(2,2));
console.log(divide(2,2));
console.log(parseToInt("2"));
